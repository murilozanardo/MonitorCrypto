require 'net/http'
require 'json'

class Crypto
  def initialize(cryptocurrencies, currencies)
    @cryptocurrencies = cryptocurrencies.flatten.map(&:upcase)
    @currencies = currencies.flatten.map(&:upcase)
  end

  def monitor(time)
    loop do
      system("clear") || system("cls")
      puts "Atualizado em: #{Time.now.strftime('%H:%M:%S')}"
      actual_price
      sleep(time)
    end
  end

  def actual_price
    @cryptocurrencies.each do |crypto|
      prices = fetch_crypto_price(crypto)
      @currencies.each do |currency|
        puts "O preço atual da #{crypto} em #{currency} é #{currency} #{prices[currency]}"
      end
      puts "\n\n"
    end
  end

  private

  def fetch_crypto_price(crypto)
    url = URI("https://min-api.cryptocompare.com/data/price?fsym=#{crypto}&tsyms=#{@currencies.join(',')}")
    response = Net::HTTP.get(url)
    JSON.parse(response)
  end
end
